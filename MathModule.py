def sum(num1, num2):
    # This function adds two numbers and return the result
    result = num1 + num2
    return result

def subtraction(num1, num2):
    # This function subtracts two numbers and return the result
    result = num1 - num2
    return result

def multiplication(num1, num2):
    # This function multiply two numbers and return the result
    result = num1 * num2
    return result

def division(num1, num2):
    # This function divide two numbers and return the result
    result = num1 / num2
    result = round(result, 3)
    return result